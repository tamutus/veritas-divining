    <h1>Code of Ethical Conduct for Tarot Professionals</h1>

    <section>
      <h2>Mission</h2>

      <p>
        This Tarot Professional strives to uphold the highest ethical standards
        in all services rendered and to act affirmatively toward maintaining
        public trust in the tarot profession. Cres Veritas’ practice is grounded
        on use of the tarot as a tool to assist clients with tapping into their
        own intuitions, spirituality, or creativity.
      </p>

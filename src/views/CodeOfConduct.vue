    <section>
      <h2>Article One | Professionalism</h2>

      <h3>Scope of Profession.</h3>

      <p>
        The tarot professional applies tarot in a way that falls outside the
        scope of occult sciences and is not reading tarot in a manner that
        claims or purports to tell fortunes, or with the ability to foretell
        with any certainty future events, or disclose in unequivocal terms the
        mental faculties of any persons.
      </p>

      <h3>Role of the Tarot Professional.</h3>

      <p>
        The tarot professional provides a client with spiritual guidance and
        serves to empower the client toward making more spiritually empowered
        decisions for themself.
      </p>

      <h3>Limitations.</h3>

      <p>
        A tarot professional shall never claim the ability to cure another of
        ailments outside the lawful practice of medicine or provide financial
        benefits outside the lawful practice of financial analysis and
        management. The tarot professional shall never purport to offer legal,
        medical, or financial advice. Unless the tarot professional also
        possesses the applicable educational background, licensures,
        certifications, and/or qualifications to be a psychologist, therapist,
        counselor, or other mental health professional, they shall not purport
        to be one.
      </p>

      <h3>Referrals to Qualified Professionals.</h3>

      <p>
        A tarot professional owes a reasonable duty of care to his or her client
        and if an inquiry touches upon legal matters, even by a tangential
        connection or in passing, the tarot professional will refer the client
        to seek the counsel of a licensed attorney. If an inquiry touches upon
        health or medical matters, even by a tangential connection or in
        passing, the tarot professional will refer the client to seek diagnosis
        by a licensed medical doctor. If an inquiry touches upon financial or
        real property matters, even by a tangential connection or in passing,
        the tarot professional will refer the client to seek advice from a
        qualified financial consultant, analyst, or real estate professional.
      </p>

      <h3>Duty to Reject Subsequent Reading Requests.</h3>

      <p>
        If at any time a tarot professional believes or has reason to believe
        that a client is using tarot readings to substitute, and not merely
        supplement, counseling from a qualified mental health professional, then
        he or she has a duty to reject subsequent reading requests from the
        client and to refer the client to seek qualified mental professional
        support. However, tarot readings may be used as a supplement to
        qualified counseling or serve the function of life coaching or general
        spiritual guidance.
      </p>

      <h3>No Dishonesty, Fraud, Deceit, or Misrepresentations.</h3>

      <p>
        A tarot professional shall not use tarot in a way that purports
        definitive and absolute knowledge of the future or of matters that are
        otherwise hidden or occult. The tarot professional shall not represent
        him or herself as matter-of-factly having communication, access to, or
        knowledge of any supernatural phenomena. The tarot professional shall
        never guarantee any one outcome, positive or negative. A tarot
        professional shall never engage in deceptive business or unfair trade
        practices. A tarot professional shall make no misrepresentations about
        his or her competency, certifications, qualifications, or abilities.
      </p>

      <h3>Third Party Consent.</h3>

      <p>
        A tarot professional is discouraged from providing reading services on
        inquiries into the personal affairs, business, intentions, mental
        faculties, or activities of third parties who are not present for the
        reading, unless the client represents and warrants to the tarot
        professional that such third party’s express consent has been obtained.
        A tarot professional may reasonably rely on such a representation made
        by a client, though best practices would include noting the reliance in
        writing in the tarot professional’s notes. However, a tarot professional
        may provide reading services on inquiries about how the client’s
        affairs, business, intentions, mental faculties, or activities might be
        affected by that third party.
      </p>
    </section>

    <section>
      <h2>Article Two | Communications</h2>

      <h3>The Reading Process.</h3>

      <p>
        A tarot professional is encouraged to make reasonable efforts to
        communicate his or her reading or interpretive process and analytical or
        intuitive methods, particular traditions, or the interpretive framework
        utilized. A tarot professional will refrain from communicating to a
        client about curses or the manifestation of malicious intentions through
        supernatural or occult means.
      </p>

      <h3>Disclosures.</h3>

      <p>
        A tarot professional shall make reasonable efforts to disclose to all
        clients that tarot readings cannot replace the counseling services of a
        mental health professional, and a tarot professional can never guarantee
        his or her accuracy in the interpretation of a reading. Also, if and
        when tarot practice would be explicitly barred by a jurisdiction’s
        applicable laws, then the tarot professional must communicate to the
        client in clear, unambiguous terms that such readings are to be for
        entertainment purposes. See also Article Five, Disclosures.
      </p>

      <h3>No Duty to Retain Records.</h3>

      <p>
        A tarot professional does not owe a client any duty to retain the
        client’s reading records. A tarot professional may, at the discretion of
        his or her customary practice, retain records or files on client
        readings.
      </p>

      <h3>Records Retention.</h3>

      <p>
        If a tarot professional does elect to retain client records, then he or
        she must develop and maintain appropriate record-keeping systems for
        storing his or her clients’ personal information in a manner that will
        reasonably preserve the clients’ confidentiality and privacy.
      </p>
    </section>

    <section>
      <h2>Article Three | Fees</h2>

      <h3>Statement of Fees.</h3>

      <p>
        A tarot professional shall state his or her service fees before the
        start of a reading and cannot later raise or increase the stated fee
        unless prior contractual terms allow for the increase. The tarot
        professional must clearly communicate his or her fee schedule and
        payment terms prior to the commencement of any reading. A fee for any
        service that is $500.00 USD or more must be contractually agreed upon in
        writing.
      </p>

      <h3>Referral Fees.</h3>
      <p>
        If a tarot professional makes a referral, then they must disclose
        whether any referral fees have been paid as compensation or
        consideration for the referral.
      </p>
    </section>

    <section>
      <h2>Article Four | Confidentiality</h2>

      <h3>Duty of Confidentiality.</h3>

      <p>
        Although there is not likely to be a legal evidentiary privilege between
        a tarot professional and client, a tarot professional nonetheless agrees
        on his or her own accord to be bound by a duty of confidentiality. A
        tarot professional shall not reveal personal or private information
        relating to a client reading unless that client has given informed
        consent.
      </p>

      <h3>Reasonable Diligence to Protect Confidentiality.</h3>

      <p>
        The tarot professional shall exercise reasonable efforts to prevent the
        inadvertent or unauthorized disclosure of or access to information
        relating to his or her clients, which includes password protection to
        any electronic accounts, computers, or data storage units that would
        include client files and lock and key to any physical storage units
        containing hard copy files.
      </p>

      <h3>Exceptions.</h3>

      <p>
        However, tarot professionals may reveal personal or otherwise private
        information relating to a client reading if the tarot professional
        reasonably believes it is necessary to prevent substantial bodily harm
        or death, or a court order overrides the confidentiality. Partial
        information from a tarot reading may be disclosed under the client’s
        anonymity for academic, civic, census, survey, or research purposes.
      </p>
    </section>

    <section>
      <h2>Article Five | Disclosures</h2>

      <h3>General Practice Disclosures.</h3>

      <p>
        A tarot professional shall post a disclosure statement prominently at
        office locations, storefronts, on websites, reading venues, or
        commercial marketing materials that states essentially as follows:
      </p>

      <Quote>
        <h4>Disclosure Statement:</h4>

        <p>
          A tarot professional cannot predict the future with certainty and you
          should not rely on a tarot reading to make any decision that would
          affect your legal, financial, or medical condition. If your inquiry
          involves the law, finance, or medicine, then you should seek the
          advice of a licensed or qualified legal, financial, or medical
          professional. Also, tarot reading cannot replace qualified mental
          health care. A tarot reading can only facilitate how you cope
          spiritually with a given situation. In certain jurisdictions, a tarot
          professional is required to disclose to you that tarot readings should
          be for entertainment purposes only, and if such a law applies to your
          reading, then you are hereby on notice thereof. Your tarot
          professional is bound by a self-imposed code of ethics, which can be
          provided to you upon request.
        </p>
      </Quote>

      <h3>Certification Disclosures.</h3>

      <p>
        A tarot professional is not required to be certified. However, if he or
        she obtains a certification related to tarot practice, the tarot
        professional will disclose the certifying body or organization that
        issued the certification.
      </p>
    </section>

    <section>
      <h2>Article Six | Public Service</h2>

      <p>
        A tarot professional is encouraged to dedicate some of their practice
        per year to public service, such as offering reading services to support
        fundraisers for non-profit organizations or charitable causes, free
        readings to persons of limited financial means, or participating in
        activities with the mission of improving the practice of tarot or the
        tarot profession.
      </p>
    </section>

    <section>
      <h2>Article Seven | Continuing Education</h2>

      <p>
        A tarot professional shall take the initiative to continue their tarot
        education for the full duration of and throughout their professional
        practice. A tarot professional is encouraged to take classes or
        workshops on tarot, attend tarot conferences, symposia, or conventions,
        meet and collaborate with other tarot professionals, regularly read on
        the practice of tarot, and exercise diligent efforts to hone the tarot
        professional’s craft and skill.
      </p>
